import DOMPurify from 'dompurify';

const ALLOWED_TAGS = [
  'a',
  'b',
  'blockquote',
  'br',
  'caption',
  'col',
  'colgroup',
  'div',
  'em',
  'figcaption',
  'figure',
  'h1',
  'h2',
  'h3',
  'h4',
  'h5',
  'h6',
  'hr',
  'i',
  'iframe',
  'img',
  'li',
  'mark',
  'ol',
  'p',
  's',
  'span',
  'strong',
  'sub',
  'sup',
  'table',
  'tbody',
  'td',
  'tfoot',
  'th',
  'thead',
  'tr',
  'u',
  'ul',
  'input',
  'label',
  'video',
  'source',
];

const ALLOWED_ATTR = [
  'href',
  'target',
  'rel',
  'src',
  'alt',
  'title',
  'width',
  'height',
  'class',
  'style',
  'colspan',
  'rowspan',
  'data-align',
  'data-wrap',
  'data-width',
  'data-caption',
  'data-checked',
  'data-training-video',
  'data-provider',
  'data-training-image',
  'type',
  'checked',
  'disabled',
  'loading',
  'decoding',
  'controls',
  'playsinline',
  'preload',
  'poster',
  'allow',
  'allowfullscreen',
  'frameborder',
];

export function sanitizeTrainingHtml(html: string): string {
  return DOMPurify.sanitize(html, {
    ALLOWED_TAGS,
    ALLOWED_ATTR,
    ALLOW_DATA_ATTR: true,
    ADD_ATTR: ['target', 'allowfullscreen', 'controls', 'playsinline'],
    ADD_TAGS: ['iframe', 'video', 'source'],
  });
}
